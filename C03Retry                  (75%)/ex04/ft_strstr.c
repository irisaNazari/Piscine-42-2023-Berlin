#include <unistd.h>

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	k;
	int	j;

	if (to_find[0] == '\0')
	{
		return (str);
	}
	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		k = i;
		while (str[k] == to_find[j] && to_find[j] != '\0')
		{
			k++;
			j++;
		}
		if (to_find[j] == '\0')
		{
			return (&str[i]);
		}
		i++;
	}
	return (0);
}
