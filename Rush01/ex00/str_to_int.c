int	str_to_int(char *str)
{
	int	result;
	int	i;

	result = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '1' && str[i] <= '4')
		{
			result = (str[i] - '0');
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (result);
}
